let day = 8;

let dayName;

switch (day) {
    case 1:
        dayName = "Saturday";
        break;
    case 2:
        dayName = "Sunday";
        break;
    case 3:
        dayName = "Monday";
        break;
    case 4:
        dayName = "Tuesday";
        break;
    case 5:
        dayName = "Wednesday";
        break;
    case 6:
        dayName = "Thursday";
        break;
    case 7:
        dayName = "Thursday";
        break;
    default :
        dayName = "Invalid Day";
        break;
}

console.log(dayName);